# power
for i in range(0, 11):
    print(
        """{0} \t \tto the power of 0 is {1}, 
        to the power of 1 is {2}, 
        to the power of 2 (squared) is {3}, 
        to the power of 3 (cubed) is {4}""".format(
            i, i ** 0, i ** 1, i ** 2, i ** 3))

